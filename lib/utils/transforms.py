def adjust_aspect_ratio(s, aspect_ratio, fit_short_side=False):
    w, h = s[0], s[1]
    if w > aspect_ratio * h:
        if fit_short_side:
            w = h * aspect_ratio
        else:
            h = w * 1.0 / aspect_ratio
    elif w < aspect_ratio * h:
        if fit_short_side:
            h = w * 1.0 / aspect_ratio
        else:
            w = h * aspect_ratio
    return np.array([w, h])

def hoe_heatmap_gen(label, output_len, gaussian_kernel=11, sigma=1.7):
    gaussian_kernel = 6 * int(sigma) + 1
    ret = np.zeros((output_len), dtype='float32')
    kernel = signal.gaussian(gaussian_kernel, sigma)
    mid_point = int(gaussian_kernel / 2)
    for j in range(-mid_point, mid_point + 1):
        idx = (label + j + output_len) % output_len
        ret[idx] = kernel[j + mid_point]
    ret = ret/ret.sum()
    return ret